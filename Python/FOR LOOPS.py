#FOR LOOP FOR NAME(STRING)
name = "Saish"
for i in name:
    print(i)

#FOR LOOP FOR COLOURS(ARRAY)
colors = ["Red","Blue","Green","Yellow"]
for colour in colors:
    print(colour)
    for j in colour:
        print(j)

#FOR LOOP FOR RANGE(INT OR NUMBER EXAMPLE_1)
for s in range(10):
    print(s)

#FOR LOOP FOR RANGE(EXAMPLE_2)
for s in range(10):
    print(s+1)

#FOR LOOP FOR RANGE(EXAMPLE_3)
for b in range (1,12,2):
    print(b)